    private void save_tour_packageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_tour_packageActionPerformed
        // TODO add your handling code here:
        String description = tour_description.getText();
        String location = tour_location.getText();
        double guideCost = 0.0;
        double ticket = 0.0;
        if (description.isEmpty()) {
            this.showWarning("Description is required");
            return;
        }
        if (location.isEmpty()) {
            this.showWarning("Location is required");
            return;
        }
        if (tour_cost.getText().isEmpty()) {
            this.showWarning("Tour cost is required");
            return;
        }
        double tourCost = Double.parseDouble(tour_cost.getText());
        if(tourPackageType.isEmpty()) {
            this.showWarning("Please choose a package type: either city tour or sightseein tour");
            return;
        }
        if (tourPackageType.equals("cityTour")) {
            if (guide_cost.getText().isEmpty()){
                this.showWarning("Guide cost is required");
                return;
            }
            if (withGuide) {
                guideCost = Double.parseDouble(guide_cost.getText());
            }
            cityTour = new CityTour(withGuide, guideCost, description, location, tourCost);
            tourPackages.add(cityTour);
            packageCombo.addItem(cityTour.toString());
        }else if (tourPackageType.equals("sightSeeing")) {
            if (ticketCost.getText().isEmpty()){
                this.showWarning("Ticket cost is required");
                return;
            }
            ticket = Double.parseDouble(ticketCost.getText());
            sightseeingTour = new SightseeingTour(ticket, description, location, tourCost);
            tourPackages.add(sightseeingTour);
            packageCombo.addItem(sightseeingTour.toString());
        }
        JOptionPane.showMessageDialog(this, "Package created");
    }//GEN-LAST:event_save_tour_packageActionPerformed

