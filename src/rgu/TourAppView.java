    private void create_bookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_bookingActionPerformed
        // TODO add your handling code here:
        try{
            String dateTime = date_time.getText();
            if (dateTime.equals("")) this.showWarning("date and time is required");
            int touristIndex = touristCombo.getSelectedIndex();
            int packageIndex = packageCombo.getSelectedIndex();
            Date bookDate = new SimpleDateFormat("dd-M-yyyy hh:mm:ss").parse(dateTime);
            // get the object of tourist and tourpackage from their list and create the booking
            Booking newBooking = new Booking(tourists.get(touristIndex), tourPackages.get(packageIndex), bookDate);
            bookings.add(newBooking);
            bookingsCombo.addItem(newBooking.toString());
            JOptionPane.showMessageDialog(this, "Booking created");
        }catch(IllegalArgumentException iae){
            this.showWarning(iae.toString());
        } catch (ParseException ex) {
            Logger.getLogger(TourAppView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_create_bookingActionPerformed

    private void bookingsComboPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_bookingsComboPopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        // get the index of the booking.
        int bookingIndex = bookingsCombo.getSelectedIndex();
        Booking selectedBooking = bookings.get(bookingIndex);
        //get the tourist
        Tourist selectedTourist = selectedBooking.getTourist();
        // display touristt properties
        tourist_name.setText(selectedTourist.getName());
        tourist_country.setText(selectedTourist.getCountry());
        languageCombo.setSelectedItem(selectedTourist.getAudioLanguage());
        // get the tourPackage
        TourPackage selectedTourPackage = selectedBooking.getTourPackage();
        // display tour package properties
        tour_description.setText(selectedTourPackage.getDescription());
        tour_location.setText(selectedTourPackage.getLocation());
        tour_cost.setText(String.valueOf(selectedTourPackage.getCost()));
        // Check for type of selected tour package and display accordingly
        if(selectedTourPackage instanceof CityTour){
            cityRadio.setSelected(true);
            CityTour selectedCityTour = (CityTour) selectedTourPackage;
            if(selectedCityTour.isWithGuide()) {
                   yesRadio.setSelected(true);
                   guide_cost.setText(String.valueOf(selectedCityTour.getGuideCost()));
            }else{
                noRadio.setSelected(true);
            }
            sightRadio.setSelected(false);
            ticketCost.setText("");
            
        }else{
            SightseeingTour selectedSiteTour = (SightseeingTour) selectedTourPackage;
            sightRadio.setSelected(true);
            ticketCost.setText(String.valueOf(selectedSiteTour.getTicketCost()));
            cityRadio.setSelected(false);
            yesRadio.setSelected(false);
            noRadio.setSelected(false);
            guide_cost.setText("");
            
        }
    }//GEN-LAST:event_bookingsComboPopupMenuWillBecomeInvisible

