    private void loadInitialData(){
        tourPackages = new ArrayList();
        cityTour = new CityTour(true, 500.0, "A city tour in california", "California USA", 2000.0);
        sightseeingTour = new SightseeingTour(200.0, "A sightseeing tour in canada", "Montreal Canada", 1000.0);
        tourPackages.add(cityTour);
        tourPackages.add(sightseeingTour);
        tourist = new Tourist("Mark Joe", "USA", AudioLanguage.ENGLISH);
        tourists = new ArrayList();
        tourists.add(tourist);
        Date bookingDate = new Date();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-M-yyy hh:mm:ss");
        String dateTime = dateFormat.format(bookingDate);
        date_time.setText(dateTime); //display current date and time
        Booking firstBooking = new Booking(tourist, sightseeingTour, bookingDate);
        CityTour customCityTour = new CityTour(false, 300.0, "A custom city tour with no guide in India", "Taj Mahal India", 1500.0);
        tourPackages.add(customCityTour);
        Booking secondBooking = new Booking(tourist, customCityTour, new Date());
        bookings = new ArrayList();
        bookings.add(firstBooking);
        bookings.add(secondBooking);
        // Iterate the lists and display content
        for (Tourist tourTourist : tourists){
            touristCombo.addItem(tourTourist.toString());
        }
        for (TourPackage tp : tourPackages){
            packageCombo.addItem(tp.toString());
        }
        // sort bookings according to date andt ime
        Collections.sort(bookings);
        for (Booking book : bookings ){
            bookingsCombo.addItem(book.toString());
        }
    }
    
    private void loadAudioLanguage(){
        for (AudioLanguage value : AudioLanguage.values()){
            languageCombo.addItem(value.toString());
        }
    }
    
